package com.panasonic.b2bacns.bizportal.dashboard.vo;

/**
 * This class is used for IDUInfo data
 * 
 * @author akansha
 *
 */
public class IDUInfo {

	private Integer totalIDU;

	private Integer totalInactiveIDU;

	private Integer notificationCountOfIDU;

	/**
	 * @return the totalIDU
	 */
	public Integer getTotalIDU() {
		return totalIDU;
	}

	/**
	 * @param totalIDU
	 *            the totalIDU to set
	 */
	public void setTotalIDU(Integer totalIDU) {
		this.totalIDU = totalIDU;
	}

	/**
	 * @return the totalInactiveIDU
	 */
	public Integer getTotalInactiveIDU() {
		return totalInactiveIDU;
	}

	/**
	 * @param totalInactiveIDU
	 *            the totalInactiveIDU to set
	 */
	public void setTotalInactiveIDU(Integer totalInactiveIDU) {
		this.totalInactiveIDU = totalInactiveIDU;
	}

	/**
	 * @return the notificationCountOfIDU
	 */
	public Integer getNotificationCountOfIDU() {
		return notificationCountOfIDU;
	}

	/**
	 * @param notificationCountOfIDU
	 *            the notificationCountOfIDU to set
	 */
	public void setNotificationCountOfIDU(Integer notificationCountOfIDU) {
		this.notificationCountOfIDU = notificationCountOfIDU;
	}

}

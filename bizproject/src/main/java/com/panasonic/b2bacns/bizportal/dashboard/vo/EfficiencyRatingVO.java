package com.panasonic.b2bacns.bizportal.dashboard.vo;

public class EfficiencyRatingVO {

	private String groupName;
	private String treesCount;
	private String coolingEfficiency;
	private String heatingEfficiency;

	/**
	 * @return the groupName
	 */
	public String getGroupName() {
		return groupName;
	}

	/**
	 * @param groupName
	 *            the groupName to set
	 */
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}

	/**
	 * @return the treesCount
	 */
	public String getTreesCount() {
		return treesCount;
	}

	/**
	 * @param treesCount
	 *            the treesCount to set
	 */
	public void setTreesCount(String treesCount) {
		this.treesCount = treesCount;
	}

	/**
	 * @return the coolingEfficiency
	 */
	public String getCoolingEfficiency() {
		return coolingEfficiency;
	}

	/**
	 * @param coolingEfficiency
	 *            the coolingEfficiency to set
	 */
	public void setCoolingEfficiency(String coolingEfficiency) {
		this.coolingEfficiency = coolingEfficiency;
	}

	/**
	 * @return the heatingEfficiency
	 */
	public String getHeatingEfficiency() {
		return heatingEfficiency;
	}

	/**
	 * @param heatingEfficiency
	 *            the heatingEfficiency to set
	 */
	public void setHeatingEfficiency(String heatingEfficiency) {
		this.heatingEfficiency = heatingEfficiency;
	}

}
